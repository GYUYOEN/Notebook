package exam09_N;

public class Triangle_N {
	
	public double area(double width) {
		return (width * width) / 2;
	}
	
	public double round(double width, double height) {
		double result;
		result = (width + height) / 2;
		return result;
	}
	
	public double round(double length) {
		double result;
		result = length * 3;
		return result;
	}

}
